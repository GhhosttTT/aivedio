"""
增强版剧本生成 Prompt 模板

支持章节结构和详细描述，提升剧本质量和图像生成效果
"""

from typing import Optional
from src.utils.logger import logger


def generate_enhanced_script_prompt(
    theme: Optional[str] = None,
    outline: Optional[str] = None,
    num_scenes: int = 10,
    num_characters: int = 2,
    style: str = "现代都市",
    num_chapters: int = 3
) -> str:
    """
    构建增强版剧本生成的 Prompt（支持章节结构和详细描述）
    
    Args:
        theme: 主题关键词（可选）
        outline: 故事大纲（可选）
        num_scenes: 分镜数量（默认 10）
        num_characters: 角色数量（默认 2）
        style: 风格偏好（默认"现代都市"）
        num_chapters: 章节数量（默认 3）
        
    Returns:
        完整的 Prompt 文本
        
    Raises:
        ValueError: 如果 theme 和 outline 都为空
    """
    if not theme and not outline:
        raise ValueError("主题和大纲至少需要提供一个")
    
    # 计算每章分镜数量
    scenes_per_chapter = num_scenes // num_chapters
    remaining_scenes = num_scenes % num_chapters
    
    # 构建增强版 Prompt 模板
    prompt_template = """你是一位专业的短剧编剧和视觉导演。请根据以下要求创作一个完整的短剧剧本，像写书一样分章节组织：

【创作要求】
主题：{theme}
故事大纲：{outline}
总分镜数量：{num_scenes}
章节数量：{num_chapters}（每章约 {scenes_per_chapter} 个分镜）
角色数量：{num_characters}
风格：{style}

【输出格式】

【故事大纲】
（200-300字的完整故事概述，必须包含：
1. 背景设定：时代、地点、社会环境
2. 主要冲突：核心矛盾是什么
3. 情节发展：故事如何推进（开端→发展→高潮→结局）
4. 结局走向：最终如何解决
5. 主题思想：想要表达什么）

【角色】
- 角色1：[姓名]
  * 年龄性别：[例如：25岁，男性]
  * 外貌特征：[详细描述五官、发型、身材、肤色等，至少30字。例如：浓眉大眼，黑色短发自然蓬松，身材修长匀称，皮肤白皙，眼神深邃有神，鼻梁挺直，嘴唇薄而线条分明]
  * 服装风格：[详细描述服装款式、颜色、材质、配饰，至少30字。例如：黑色修身西装，意大利羊毛面料，白色纯棉衬衫，深蓝色丝质领带，银色领带夹，黑色牛皮皮鞋，手腕佩戴银色机械手表]
  * 性格特点：[至少20字。例如：冷静理性，做事一丝不苟，不善言辞但内心温柔，对工作极度认真，对感情却显得笨拙]
  * 角色定位：[例如：男主角，跨国公司高管，28岁单身]

- 角色2：[姓名]
  * 年龄性别：[例如：23岁，女性]
  * 外貌特征：[详细描述，至少30字]
  * 服装风格：[详细描述，至少30字]
  * 性格特点：[至少20字]
  * 角色定位：[例如：女主角，独立设计师]

（如果有更多角色，继续按此格式添加）

【第一章：[章节标题，例如：孤独的夜晚]】
章节概述：[100-150字，必须包含：
1. 本章的主要情节（发生了什么）
2. 情感基调（整体氛围是什么）
3. 关键转折点（有什么重要变化）
4. 与整体故事的关系（在故事中的作用）]

分镜1：
- 章节位置：第一章 开场
- 故事节点：[30-50字，说明本分镜在故事中的作用。例如：男主角独自在办公室加班到深夜，通过他的工作状态和周围环境，展现他的孤独感和对工作的执着，为后续与女主角的相遇做铺垫]
- 环境描述：[至少50字，必须包含：
  * 时间：具体时间点（例如：深夜11点）
  * 地点：详细的空间信息（例如：现代化办公室，30层高楼）
  * 天气：天气状况（例如：窗外细雨绵绵）
  * 光线：自然光或人造光（例如：只有一盏台灯亮着）
  * 具体物品：至少3个可见物品（例如：文件、咖啡杯、时钟）
  * 背景元素：远景、中景、近景的元素（例如：落地窗外的城市夜景）
  * 空间布局：物体的相对位置（例如：办公桌靠近落地窗）
  示例：深夜11点的现代化办公室，位于30层高楼，落地窗外是璀璨的城市夜景，细雨在玻璃上形成水珠，室内只有一盏台灯亮着，桌上堆满文件和半空的咖啡杯，空调发出低沉的嗡嗡声，墙上的时钟指针缓慢移动，书架上整齐摆放着各类书籍，地面是深色木地板，反射着微弱的灯光]
- 人物描述：[至少40字，必须包含：
  * 位置：角色在场景中的位置
  * 姿势：身体姿态（坐/站/躺，身体角度）
  * 表情：面部表情细节（眉毛、眼神、嘴角）
  * 动作：正在做什么（具体动作）
  * 服装细节：当前穿着的具体描述
  * 肢体语言：传达的情绪（紧张/放松/疲惫）
  * 情绪状态：内心状态的外在表现
  示例：男主角坐在黑色真皮办公椅上，身体微微前倾，左手撑着额头，右手握着钢笔在文件上快速书写，眉头紧锁，眼神专注而疲惫，白色衬衫的袖子卷到手肘，领带松开挂在脖子上，西装外套搭在椅背上，整个人散发出疲惫但专注的气质，偶尔抬头看向窗外，眼神中流露出一丝孤独]
- 镜头描述：[至少20字，必须包含：
  * 景别：特写/中景/全景
  * 拍摄角度：正面/侧面/背面，俯视/平视/仰视，具体角度（例如：侧前方30度）
  * 构图：三分法、对称、黄金分割等
  * 焦点：焦点在哪里（人物脸部/手部/背景）
  * 景深：浅景深/深景深，前景/背景虚化情况
  示例：中景镜头，从侧前方30度角拍摄，人物位于画面右侧三分之一处，左侧是虚化的办公室背景，焦点在人物脸部，景深较浅，营造电影感，前景有轻微虚化的文件堆]
- 光线描述：[至少20字，必须包含：
  * 光源类型：自然光/人造光（台灯/吊灯/霓虹灯）
  * 光源位置：从哪个方向照射（左侧/右侧/顶部/背后）
  * 色温：暖色（黄色/橙色）/冷色（蓝色/白色）
  * 明暗对比：高对比/低对比
  * 阴影效果：硬阴影/软阴影，阴影的位置和形状
  示例：主光源是台灯的暖黄色光线从左侧照射，在脸部形成柔和的明暗对比，背景的城市灯光透过落地窗形成冷色调的轮廓光，整体呈现暖冷对比的视觉效果，高对比度，脸部右侧有柔和的阴影]
- 氛围描述：[至少20字，必须包含：
  * 情绪基调：孤独/温暖/紧张/浪漫/悲伤/欢快等
  * 视觉风格：电影级/纪实/梦幻/复古/现代等
  * 色彩倾向：暖色调/冷色调/高饱和/低饱和/黑白/单色
  * 质感：细腻/粗糙/柔和/锐利/朦胧/清晰
  * 电影感：类似哪部电影的视觉风格
  示例：孤独、专注、疲惫的氛围，电影级画质，暖黄与冷蓝的色彩对比，高对比度，细腻的光影层次，真实感强，类似《社交网络》的视觉风格，带有都市夜晚的寂寥感]
- 出现角色：[角色1]
- 对话：（如果有对话，写在这里；如果没有对话，写"无"）
- 说话人：（如果有对话，标注说话人；如果没有对话，写"无"）
- 情感：[例如：疲惫、专注、孤独]

分镜2：
- 章节位置：第一章 发展
- 故事节点：[30-50字，描述本分镜在故事中的作用]
- 环境描述：[至少50字的详细描述]
- 人物描述：[至少40字的详细描述]
- 镜头描述：[至少20字的详细描述]
- 光线描述：[至少20字的详细描述]
- 氛围描述：[至少20字的详细描述]
- 出现角色：[角色列表]
- 对话：xxx
- 说话人：角色名
- 情感：xxx

（继续第一章的其他分镜...）

【第二章：[章节标题]】
章节概述：[100-150字，描述本章的主要情节]

分镜X：
（按照相同格式继续）

【第三章：[章节标题]】
章节概述：[100-150字，描述本章的主要情节]

分镜Y：
（按照相同格式继续）

【剧本完整性检查】
- 故事结构：开端 → 发展 → 高潮 → 结局 ✓
- 角色弧光：主角是否有成长或变化 ✓
- 情节连贯：每个分镜是否承接上一个分镜 ✓
- 视觉丰富：场景是否多样化，避免重复 ✓
- 情感递进：情感是否有层次和变化 ✓

【重要要求】
1. 【故事大纲】必须完整，200-300字，涵盖整个故事的起承转合
2. 【角色】描述必须极其详细，每个角色至少100字，包含所有外貌、服装、性格细节
3. 【章节】结构清晰，每章有独立的情节推进和情感基调，章节概述100-150字
4. 【分镜】描述必须达到以下字数要求：
   - 故事节点：30-50字
   - 环境描述：至少50字
   - 人物描述：至少40字
   - 镜头描述：至少20字
   - 光线描述：至少20字
   - 氛围描述：至少20字
   - 每个分镜总计至少200字
5. 每个分镜必须包含"故事节点"，说明它在整体故事中的作用
6. 环境描述必须包含：时间、地点、天气、光线、具体物品（至少3个）、背景元素、空间布局
7. 人物描述必须包含：位置、姿势、表情、动作、服装细节、肢体语言、情绪状态
8. 镜头描述必须包含：景别、拍摄角度、构图、焦点、景深
9. 光线描述必须包含：光源类型、位置、色温、明暗对比、阴影效果
10. 氛围描述必须包含：情绪基调、视觉风格、色彩倾向、质感、电影感
11. 对话要自然流畅，符合角色性格，推动剧情发展
12. 每个分镜时长约 3-5 秒
13. 风格要符合"{style}"的特点
14. 严格按照上述格式输出，不要添加额外的说明文字
15. 所有描述都要详细具体，达到"可视化小说"的水平，让 AI 绘画能准确还原场景
16. 分镜之间要有连贯性，避免场景跳跃过大，确保视觉和情节的流畅过渡
17. 每章结尾要有情节转折或情感高潮，吸引观众继续观看
18. 角色描述要保持一致性，同一角色在不同分镜中的外貌和服装要统一（除非剧情需要改变）
19. 使用具体的视觉细节，避免抽象描述（例如：不要说"很美"，而要说"长发如瀑布般垂落，在灯光下泛着柔和的光泽"）
20. 每个分镜都要有明确的视觉焦点，避免描述过于分散

【创作提示】
- 把剧本当作一本"可视化小说"来写，而不是简单的分镜列表
- 每个分镜都要能让读者在脑海中形成清晰的画面
- 注重细节描写，但要避免冗余和重复
- 情节要有起伏，情感要有层次
- 角色要有成长和变化，不能一成不变
- 场景要多样化，避免视觉疲劳
- 对话要推动剧情，不要为了对话而对话
- 每章要有独立的小高潮，整体要有大高潮
- 结局要有回味，不要草草收场

现在开始创作吧！"""
    
    # 填充模板
    prompt = prompt_template.format(
        theme=theme or "未指定",
        outline=outline or "未指定",
        num_scenes=num_scenes,
        num_chapters=num_chapters,
        scenes_per_chapter=scenes_per_chapter,
        num_characters=num_characters,
        style=style
    )
    
    logger.info(f"构建增强版剧本生成 Prompt")
    logger.info(f"剧本结构：{num_chapters} 章，共 {num_scenes} 个分镜，每章约 {scenes_per_chapter} 个分镜")
    logger.debug(f"Prompt 长度: {len(prompt)} 字符")
    
    return prompt


